package com.khushi;

import java.util.Scanner;

public class sumtilll{
    public static void main(String[] args) {
        Scanner in=new Scanner(System.in);

        int sum=0;
        int x=0;
        while((x=in.nextInt())!=0){

            sum=sum+x;

        }
        System.out.println(sum);



    }
}
